#include "matrix.h"

t_matrix	multiply_matrix(t_matrix m1, t_matrix m2)
{
	t_matrix	res;

	res[0][0] = m1[0][0] * m2[0][0] + m1[0][0] * m2[1][0] + m1[0][0] * m2[2][0]
		+ m1[0][0] * m2[3][0];
	res[0][1] = m1[0][1] * m2[0][0] + m1[0][1] * m2[1][0] + m1[0][1] * m2[2][0]
		+ m1[0][1] * m2[3][0];
	res[0][2] = m1[0][2] * m2[0][0] + m1[0][2] * m2[1][0] + m1[0][2] * m2[2][0]
		+ m1[0][2] * m2[3][0];
	res[0][3] = m1[0][3] * m2[0][0] + m1[0][3] * m2[1][0] + m1[0][3] * m2[2][0]
		+ m1[0][3] * m2[3][0];
	res[1][0] = m1[1][0] * m2[0][1] + m1[1][0] * m2[1][1] + m1[1][0] * m2[2][1]
		+ m1[1][0] * m2[3][1];
	res[1][1] = m1[1][1] * m2[0][1] + m1[1][1] * m2[1][1] + m1[1][1] * m2[2][1]
		+ m1[1][1] * m2[3][1];
	res[1][2] = m1[1][2] * m2[0][1] + m1[1][2] * m2[1][1] + m1[1][2] * m2[2][1]
		+ m1[1][2] * m2[3][1];
	res[1][3] = m1[1][3] * m2[0][1] + m1[1][3] * m2[1][1] + m1[1][3] * m2[2][1]
		+ m1[1][3] * m2[3][1];
	res[2][0] = m1[2][0] * m2[0][2] + m1[2][0] * m2[1][2] + m1[2][0] * m2[2][2]
		+ m1[2][0] * m2[3][2];
	res[2][1] = m1[2][1] * m2[0][2] + m1[2][1] * m2[1][2] + m1[2][1] * m2[2][2]
		+ m1[2][1] * m2[3][2];
	res[2][2] = m1[2][2] * m2[0][2] + m1[2][2] * m2[1][2] + m1[2][2] * m2[2][2]
		+ m1[2][2] * m2[3][2];
	res[2][3] = m1[2][3] * m2[0][2] + m1[2][3] * m2[1][2] + m1[2][3] * m2[2][2]
		+ m1[2][3] * m2[3][2];
	res[3][0] = m1[3][0] * m2[0][3] + m1[3][0] * m2[1][3] + m1[3][0] * m2[2][3]
		+ m1[3][0] * m2[3][3];
	res[3][1] = m1[3][1] * m2[0][3] + m1[3][1] * m2[1][3] + m1[3][1] * m2[2][3]
		+ m1[3][1] * m2[3][3];
	res[3][2] = m1[3][2] * m2[0][3] + m1[3][2] * m2[1][3] + m1[3][2] * m2[2][3]
		+ m1[3][2] * m2[3][3];
	res[3][3] = m1[3][3] * m2[0][3] + m1[3][3] * m2[1][3] + m1[3][3] * m2[2][3]
		+ m1[3][3] * m2[3][3];
	return (res);
}

t_matrix	add_sub_matrix(t_matrix m1, t_matrix m2, bool add)
{
	t_matrix	new;
	int			i;
	int			j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (add)
				new[i][j] = m1[i][j] + m2[i][j];
			else
				new[i][j] = m1[i][j] - m2[i][j];
			j++;
		}
		i++;
	}
	return (new);
}


